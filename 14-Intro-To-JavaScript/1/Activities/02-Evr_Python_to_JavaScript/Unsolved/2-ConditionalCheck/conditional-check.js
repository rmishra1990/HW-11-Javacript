var x = 25;
var y = 100;

// Checks if one value is equal to another
if (x===25){
    console.log("x is equal to 25")
}

// Checks if one value is NOT equal to another
if (x!==25){
    console.log("x is not equal to 25")
}

// Checks if one value is less than another
if(x<30){
    console.log("x is less than 30")
}


// Checks if one value is greater than another
if(y>30){
    console.log("y is greater than 30")
}

// Checks if a value is less than or equal to another
if(x<=25){
    console.log("x is less than or equal to 25")

}

// Checks for two conditions to be met using &&
console.log(x > 30 && y > 30);

// Checks if either of two conditions is met using ||
console.log(x > 30 || y > 30);

// Nested if statements
